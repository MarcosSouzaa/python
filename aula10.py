# Concatenação
concatenacao = 'A' + 'B' + 'C'
print(concatenacao) #imprime ABC

# concatenando com espaço
nome = 'Marcos' + ' ' + 'Souza'
print(nome) 

# para concatenar string com o número, vou precisar colocar o tipo do número com str
nome = 'Marcos ' + str(56) + ' ' + 'Souza'
print(nome)

# para fazer a repetição, posso usar o sinal * multiplicação
a_dez_vezes = 'A' * 10
print(a_dez_vezes)

tres_vezes_marcos = 'Marcos' * 3
print(tres_vezes_marcos)